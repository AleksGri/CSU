
# Part 1: Addition and Subtraction
#
# Pseudocode:
#
# Prompt the user to input two numbers (num1 and num2).
# Calculate the addition of num1 and num2.
# Calculate the subtraction of num1 and num2.
# Display the results of addition and subtraction.


# Taking input from the user
num1 = float(input("Enter first number (num1): "))
num2 = float(input("Enter second number (num2): "))

# Calculating addition and subtraction
addition = num1 + num2
subtraction = num1 - num2

# Displaying the results
print("Addition of the numbers:", addition)
print("Subtraction of the numbers:", subtraction)


# Part 2: Multiplication and Division
#
